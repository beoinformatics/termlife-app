// Flat list for backwards compat
export const THEME_ORDER = CATEGORY_ORDER.flatMap(cat => THEME_CATEGORIES[cat].themes)

type ThemeChangeCallback = (theme: TerminalTheme) => void

class ThemeManagerImpl {
  private _current: TerminalTheme = retroGreen
  private _currentId: string = 'retro-green'
  private _categoryId: string = 'dark'
  private _listeners: ThemeChangeCallback[] = []

  get theme(): TerminalTheme {
    return this._current
  }

  get themeId(): string {
    return this._currentId
  }

  get categoryId(): string {
    return this._categoryId
  }

  get categoryLabel(): string {
    return THEME_CATEGORIES[this._categoryId]?.label ?? this._categoryId
  }

  setTheme(id: string) {
    const theme = THEMES[id]
    if (!theme) return
    this._current = theme
    this._currentId = id
    // Update category to match
    for (const [catId, cat] of Object.entries(THEME_CATEGORIES)) {
      if (cat.themes.includes(id)) {
        this._categoryId = catId
        break
      }
    }
    for (const cb of this._listeners) {
      cb(theme)
    }
  }

  /** Cycle to the next theme within the current category */
  cycleTheme() {
    const cat = THEME_CATEGORIES[this._categoryId]
    if (!cat) return
    const idx = cat.themes.indexOf(this._currentId)
    const nextIdx = (idx + 1) % cat.themes.length
    this.setTheme(cat.themes[nextIdx])
  }

  onChange(cb: ThemeChangeCallback): () => void {
    this._listeners.push(cb)
    return () => {
      this._listeners = this._listeners.filter(l => l !== cb)
    }
  }
}

export const themeManager = new ThemeManagerImpl()
