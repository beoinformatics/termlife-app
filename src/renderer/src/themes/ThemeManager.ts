export interface TerminalTheme {
  name: string
  // Terminal colors
  background: number
  foreground: number
  cursor: number
  cursorInactive: number
  selectionBackground: number
  selectionAlpha: number
  // ANSI 16-color palette (overrides default)
  ansiPalette: number[]
  // UI chrome
  tabBarBg: number
  tabActiveBg: number
  tabInactiveBg: number
  tabActiveText: number
  tabInactiveText: number
  tabActiveIndicator: number
  tabCloseHover: number
  tabBorder: number
  buttonBg: number
  buttonHoverBg: number
  buttonIcon: number
  buttonIconHover: number
  separatorColor: number
  // Dashboard
  dashboardBg: number
  cardBg: number
  cardBorder: number
  cardPreviewBg: number
  cardPreviewBorder: number
  cardTitle: number
  cardClose: number
  cardCloseHover: number
  dashboardIcon: number
  dashboardIconHover: number
  emptyStateText: number
  // Scrollbar
  scrollbarTrack: number
  scrollbarThumb: number
  // Pane
  focusBorder: number
  dividerColor: number
  // FileBrowser
  fileBrowserBg: number
  fileBrowserHeader: number
  fileBrowserSelection: number
  fileBrowserDirectory: number
  fileBrowserFile: number
  fileBrowserHidden: number
  fileBrowserExecutable: number
  fileBrowserPreviewBg: number
  fileBrowserPreviewBorder: number
  fileBrowserPreviewText: number
  fileBrowserIcon: number
  fileBrowserIconHover: number
}

